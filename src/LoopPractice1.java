public class LoopPractice1 {
    //standard forloop but go up by 2 since ++ means add 1 to every iteration, you can just add 2 or any
    // other number every iteration.
    public static void main(String[] args) {
        for (int i = 2; i <= 10; i+=2) {
            System.out.println(i);
        }
    }

}
